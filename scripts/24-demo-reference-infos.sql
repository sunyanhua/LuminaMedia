SELECT '=== 参考信息数据统计 ===' AS step;

-- 按状态统计
SELECT
  status,
  COUNT(*) as count,
  GROUP_CONCAT(title ORDER BY publish_time DESC SEPARATOR '; ') as titles
FROM reference_infos
WHERE tenant_id = @tenant_id
GROUP BY status
ORDER BY FIELD(status, 'adopted', 'modified', 'new', 'ignored');

-- 按相关度分布统计
SELECT
  CASE
    WHEN relevance >= 90 THEN '高相关度(90-100)'
    WHEN relevance >= 80 THEN '中高相关度(80-89)'
    WHEN relevance >= 70 THEN '中相关度(70-79)'
    ELSE '低相关度(<70)'
  END as relevance_level,
  COUNT(*) as count,
  AVG(relevance) as avg_relevance
FROM reference_infos
WHERE tenant_id = @tenant_id
GROUP BY relevance_level
ORDER BY avg_relevance DESC;

-- 按分类统计
SELECT
  category,
  COUNT(*) as count
FROM reference_infos
WHERE tenant_id = @tenant_id
GROUP BY category
ORDER BY count DESC;

-- 显示最新5条记录
SELECT '=== 最新5条参考信息 ===' AS info;
SELECT
  id,
  title,
  relevance,
  status,
  is_adopted,
  DATE_FORMAT(publish_time, '%Y-%m-%d') as publish_date,
  DATE_FORMAT(created_at, '%Y-%m-%d %H:%i') as created_at
FROM reference_infos
WHERE tenant_id = @tenant_id
ORDER BY publish_time DESC
LIMIT 5;

